package com.Bridgelabz.Day1;

public class DefaultValueOfPrimitiveData {
	static int a;
	static byte b;
	static short c;
	static long d;
	static float e;
	static double f;
	static boolean g;
	static char h;

	public static void main(String[] args) {

		System.out.println("Default value of int a " + a);
		System.out.println("Default value of byte b " + b);
		System.out.println("Default value of short c " + c);
		System.out.println("Default value of long d " + d);
		System.out.println("Default value of float e " + e);
		System.out.println("Default value of double f " + f);
		System.out.println("Default value of boolean g " + g);
		System.out.println("Default value of char h " + h);
	}
}
